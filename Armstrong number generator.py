#Printing armstrong numbers between 1 and the number entered.1 and number inclusive.
n=int(input("Enter the number"))
for j in range(1,n+1):
    a=[]
    s=0
    sum=0
    num=j
    while j!=0:
        m=j%10
        j=j//10
        s+=1
        a.append(m)
    for i in range(s):
        sum+=a[i]**s
    if num==sum:
        print(num)
